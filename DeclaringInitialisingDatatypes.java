//ASSIGNMENT 10-Declaring and Initialising all datatypes 

package basicjava;

public class DeclaringInitialisingDatatypes
{
	public static void main(String[] args) 
	{
		//-2^(n-1) to 2^(n-1)
		int pincode=500028;  //n=32
		byte age=36;   //n=8
		short salary=30000;    //n=16
		long mobileno=9885317269l;   //n=16
		float percentage=99.99f;
		double pivalue=3.145678;
		char gender='a';
		boolean Apply=true;
		String name="Anusha";
        System.out.println(pincode);
		System.out.println(age);
		System.out.println(salary);
		System.out.println(mobileno);
		System.out.println(percentage);
		System.out.println(pivalue);
		System.out.println(gender);
		System.out.println(name);
		System.out.println(Apply);
	}
	
}
